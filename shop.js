function createProductItem(product) {
    return `
    <div class="item">
        <img src="${product.img}" alt="${product.name}">
        <div class="product-name">${product.name}</div>
        <div class="product-source">${product.anime}</div> 
        <div class="product-details">
            <span>Rent: ${product.rental}</span>
            <span>Normal: ${product.normal}</span>
            <span>Full: ${product.full}</span>
        </div>
         <a href="${product.link}" class="requestButton" >
           <span class="text">Buy</span>
           </a>
    </div>
    `;
}

function renderProducts(sort) {
    array.sort((a, b) => {
        switch (sort) {
            case "name_a_z":
                return a.name.localeCompare(b.name);
            case "name_z_a":
                return b.name.localeCompare(a.name);
            case "anime_a_z":
                return a.anime.localeCompare(b.anime);
            case "anime_z_a":
                return b.anime.localeCompare(a.anime);
            case "full_low_to_high":
                return a.full - b.full;
            case "full_high_to_low":
                return b.full - a.full;
            default:
                return 0;
        }
    });

    var productList = document.getElementById("container");
    productList.innerHTML = '';

    for (var i = 0; i < array.length; i++) {
        var product = array[i];
        productList.innerHTML += createProductItem(product);
    }
}

document.getElementById("sort").addEventListener("change", function() {
    renderProducts(this.value);
});

renderProducts("name_a_z");

document.getElementById('searchBox').addEventListener('input', function() {
    var searchTerm = this.value.toLowerCase();
    var items = document.querySelectorAll('.item');

    items.forEach(function(item) {
      var productName = item.querySelector('.product-name').innerText.toLowerCase();
      var productSource = item.querySelector('.product-source').innerText.toLowerCase();

      if (productName.includes(searchTerm) || productSource.includes(searchTerm)) {
        item.classList.remove('hidden');
      } else {
        item.classList.add('hidden');
      }
    });
  });
